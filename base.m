%--------------------------------------------------------------------------
%   base
%
%   constroi as funcoes de base padrao (linear) e seus gradientes,
%   onde:
%
%   grad = [ grad(phi_1) grad(phi_2) grad(phi_3) ]
%
%   e
%
%   grad(phi_i) = [ d(phi_i)/d xi d(phi_i)/d eta ]^t 
%
%   programa baseado no de Pulino, P., obtido em:
%   http://www.ime.unicamp.br/~pulino/MT623/programas/
%--------------------------------------------------------------------------
function [phi,grad] = base(xi,eta)
%--------------------------------------------------------------------------
% phi = zeros(3,1);
% grad = zeros(2,3);
%--------------------------------------------------------------------------
% phi(1) = 1.0 - xi - eta;
% phi(2) = xi;
% phi(3) = eta;
phi = [1.0 - xi - eta;xi;eta];
%--------------------------------------------------------------------------
% grad(1,1) = -1.0;
% grad(2,1) = -1.0;
% grad(1,2) =  1.0;
% grad(2,2) =  0.0;
% grad(1,3) =  0.0;
% grad(2,3) =  1.0;
 grad = [-1,1,0;-1,0,1];
%--------------------------------------------------------------------------
end
