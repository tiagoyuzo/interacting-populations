%--------------------------------------------------------------------------
%   regra de quadratura em 1d
%
%   t(:) = pontos da quadratura
%   w(:,1) = pesos da quadratura
%   nptos: numero de pontos utilizados
%--------------------------------------------------------------------------
function [w,t,nptos] = quadratura1d(iflag)
%--------------------------------------------------------------------------
%   exato para pol. de grau 3
%--------------------------------------------------------------------------
if( iflag == 1)
    
    w(1) = 5.0/9.0;
    w(2) = 8.0/9.0;
    w(3) = w(1);
    
    t(1) = -sqrt(3/5);
    t(2) = 0;
    t(3) = sqrt(3/5);
    
    nptos = 3;
%--------------------------------------------------------------------------
end
%--------------------------------------------------------------------------